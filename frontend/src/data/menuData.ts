export const getItemsByCategory = (categoryId: string): MenuItem[] => {
  return menuItems.filter((item) => item.category === categoryId);
};

export const getSignatureItems = (): MenuItem[] => {
  return menuItems.filter((item) => item.isSignature);
};

export const getFeaturedItems = (): MenuItem[] => {
  return menuItems.filter((item) => item.isSignature).slice(0, 4);
};
